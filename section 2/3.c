#include <stdio.h>

int main()
{
    int i, j, k;

    // Given i,j and k are decimal integers
    scanf("%d %d %d", &i, &j, &k);
}

int main()
{

    int i, j, k;

    // Given i is decimal, j is octal, k is hexadecimal
    scanf("%d %o %x", &i, &j, &k);
}

int main()
{
    int i, j, k;

    // Given i and j are hexadecimal and k is octal
    scanf("%x %x %o", &i, &j, &k);
}